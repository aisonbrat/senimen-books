-- Avoid rare failures if a profile row already exists when the auth trigger runs.
-- Admin-created users: trigger inserts a stub row, then service_role updates role/phone.

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, full_name)
  values (new.id, coalesce(new.raw_user_meta_data->>'full_name', ''))
  on conflict (id) do nothing;
  return new;
end;
$$;
