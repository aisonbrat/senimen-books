#!/usr/bin/env node
/**
 * Merged Cormorant italic + bold-italic TTFs for jsPDF (latin + latin-ext + cyrillic-ext + cyrillic).
 * Requires Python fontTools. Run: npm run fonts:embed-italic
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import { execFileSync } from 'child_process'
import decompress from 'wawoff2/decompress.js'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, '..')
const fontRoot = path.join(root, 'node_modules/@fontsource/cormorant/files')
const tmp = path.join(root, '.tmp-font-merge-italic')

const subsets = ['latin', 'latin-ext', 'cyrillic-ext', 'cyrillic']

async function decompressMerged(weight, style, tag) {
  const files = []
  let i = 0
  for (const sub of subsets) {
    const name = `cormorant-${sub}-${weight}-${style}.woff2`
    const fp = path.join(fontRoot, name)
    if (!fs.existsSync(fp)) throw new Error(`Missing ${name}`)
    const ttfPath = path.join(tmp, `${tag}_${i++}.ttf`)
    const buf = await decompress(fs.readFileSync(fp))
    fs.writeFileSync(ttfPath, Buffer.from(buf))
    files.push(ttfPath)
  }
  return files
}

function merge(tag, ttfPaths) {
  const out = path.join(tmp, `${tag}_merged.ttf`)
  execFileSync('python3', ['-m', 'fontTools.merge', `--output-file=${out}`, ...ttfPaths], {
    stdio: 'inherit',
    cwd: root,
  })
  return out
}

async function main() {
  fs.mkdirSync(tmp, { recursive: true })
  const italicPaths = await decompressMerged('400', 'italic', 'it')
  const boldItalicPaths = await decompressMerged('700', 'italic', 'bi')
  const italicMerged = merge('italic', italicPaths)
  const boldItalicMerged = merge('boldItalic', boldItalicPaths)
  const esc = (p) => fs.readFileSync(p).toString('base64')
  const out =
    `/* Generated by scripts/embed-cormorant-italic.mjs — merged italic subsets for jsPDF */\n` +
    `export const cormorantItalicTtfBase64 = '${esc(italicMerged)}'\n` +
    `export const cormorantBoldItalicTtfBase64 = '${esc(boldItalicMerged)}'\n`
  const dest = path.join(root, 'lib/fonts/cormorantItalicVfs.ts')
  fs.mkdirSync(path.dirname(dest), { recursive: true })
  fs.writeFileSync(dest, out)
  console.log('Wrote', dest, `(${Math.round(out.length / 1024)} KiB)`)
}

main().catch((e) => {
  console.error(e)
  process.exit(1)
})
